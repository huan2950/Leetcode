// @lc code=start
class Solution {
    public void moveZeroes(int[] nums) {
        int p = removeElement(nums, 0);
        for (int i = p; i < nums.length; i++) {
            nums[i] = 0;
        }
    }

    int removeElement(int[] nums, int val) {
        int fast = 0, slow = 0;
        while (fast < nums.length) {
            if (nums[fast] != val) {
                nums[slow] = nums[fast];
                slow++;
            }
            fast++;
        }
        return slow;
    }
}
